package com.techment.assignment;

public class Pattern11 {

	public static void main(String[] args) {
			int n=5;      
			//inner loop  
			for (int i= n; i>=1 ; i--)  
			{  
			//outer loop  
			for (int j=1; j<=i; j++)  
			{  
			//prints star and space  
			System.out.print( j+ " ");  
			}  
			  
			System.out.println();

			}

	  }
}
